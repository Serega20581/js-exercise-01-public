const multiplication = (x, y) => {
  let result = 0;
  // code
  result = x * y;
  //
  return result;
}

const calculateAverage = (value1, value2, value3, value4, value5) => {
  // code
  let sum = (value1 + value2 + value3 + value4 + value5) / 5;
  return sum;
  //
}

export { multiplication, calculateAverage };
